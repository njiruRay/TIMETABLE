    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try {
            String classname = txtClass.getText();
            
            pst = con.prepareStatement("insert into class(name)values(?)");
            pst.setString(1, classname);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Class Registration Successful");
            
            txtClass.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(School.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

